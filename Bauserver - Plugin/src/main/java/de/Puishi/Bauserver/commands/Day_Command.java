public class Day_Command implements CommandExecutor {

    @Override
    public boolean onCommand(CommandSender commandSender, Command command, String s, String[] args) {
        if(commandSender instanceof Player) {
            Player player = (Player) commandSender;
            if(player.hasPermission("bauserver.day")) {
                if(args.length == 0) {
                    for(Player all : Bukkit.getOnlinePlayers()) {
                        all.getLocation().getWorld().setTime(6000);
                        all.sendMessage(Data.getPREFIX() + "Es ist nun Tag");
                    }
                }
            } else {
                player.sendMessage(Data.getPREFIX() + "§cDazu hast du keine Rechte!");
            }
        }


        return false;
    }
}
