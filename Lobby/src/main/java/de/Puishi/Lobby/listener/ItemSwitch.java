public class ItemSwitch implements Listener {

    @EventHandler
    public void onSwitch(PlayerItemHeldEvent e){
        Player p = e.getPlayer();
        p.playSound(p.getLocation(), Sound.NOTE_STICKS, 10, 10);
    }
}
